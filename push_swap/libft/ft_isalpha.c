#include "libft.h"

static int	ft_isupper(int c)
{
	if ((unsigned int) c >= 65 && (unsigned int) c <= 90)
		return (1);
	return (0);
}

static int	ft_islower(int c)
{
	if ((unsigned int) c >= 97 && (unsigned int) c <= 122)
		return (1);
	return (0);
}

int	ft_isalpha(int c)
{
	if (ft_islower(c) || ft_isupper(c))
		return (1);
	return (0);
}
