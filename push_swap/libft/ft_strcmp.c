int	ft_strcmp(char *s1, char *s2)
{
	int	c;

	c = 0;
	while (s1[c] != '\0' || s2[c] != '\0')
	{
		if (s1[c] < s2[c])
			return ((c + 1) * -1);
		if (s1[c] > s2[c])
			return (c + 1);
		c++;
	}
	return (0);
}
