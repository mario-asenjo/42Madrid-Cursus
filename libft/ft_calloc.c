#include "libft.h"

void	*ft_calloc(size_t number, size_t size)
{
	void	*addr;

	if (number && size > SIZE_MAX / number)
		return (0);
	addr = malloc(size * number);
	if (!addr)
		return (0);
	ft_bzero(addr, size * number);
	return (addr);
}
