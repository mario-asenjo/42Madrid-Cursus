#include "libft.h"

static int	ft_isupper(int c)
{
	if ((unsigned int) c >= 65 && (unsigned int) c <= 90)
		return (1);
	return (0);
}

int	ft_tolower(int c)
{
	if (ft_isupper((char)c))
		return (c + 32);
	return (c);
}
